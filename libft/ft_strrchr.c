#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;

	a = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
			a = (char *)s;
		s++;
	}
	if (c == '\0')
		a = (char *)s;
	return (a);
}
